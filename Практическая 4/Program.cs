class Заклинание
{
    public int Мана { get; private set; }
    public string Эффект { get; private set; }

    public Заклинание(int мана, string эффект)
    {
        Мана = мана;
        Эффект = эффект;
    }

    public void Использовать()
    {
        Console.WriteLine(Эффект);
    }
}

class Волшебник
{
    public string Имя { get; private set; }
    public int Мана { get; private set; }

    public Волшебник(string имя, int мана)
    {
        Имя = имя;
        Мана = мана;
    }

    public void ПроизнестиЗаклинание(Заклинание заклинание)
    {
        if (Мана >= заклинание.Мана)
        {
            Console.WriteLine($"{Имя} колдует! {заклинание.Эффект}");
            Мана -= заклинание.Мана;
        }
        else
        {
            int необходимаяМана = заклинание.Мана - Мана;
            Console.WriteLine($"Для использования {заклинание.Эффект} не хватает {необходимаяМана} единиц маны. Хлебните зелья!");
        }
    }
}

class Посылка
{
    public string ОписаниеПосылки { get; }
    public double Вес { get; }

    public Посылка(string описание, double вес)
    {
        ОписаниеПосылки = описание;
        Вес = вес;
    }
}

class СлужбаОтправки
{
    private double ЛимитВеса;
    private double ОбщийВесОтправленныхПосылок;

    public СлужбаОтправки(double лимитВеса)
    {
        ЛимитВеса = лимитВеса;
    }

    public void ОтправитьПосылку(Посылка посылка)
    {
        if (ОбщийВесОтправленныхПосылок + посылка.Вес > ЛимитВеса)
        {
            Console.WriteLine("Ошибка: превышен лимит веса. Посылка не отправлена.");
        }
        else
        {
            ОбщийВесОтправленныхПосылок += посылка.Вес;
            Console.WriteLine($"Посылка отправлена: {посылка.ОписаниеПосылки}, Вес: {посылка.Вес} кг");
        }
    }
}

class Устройство
{
    private static int КоличествоУстройств;
    private const int БазоваяСкоростьИнтернета = 100;

    public Устройство()
    {
        КоличествоУстройств++;
    }

    public int СкоростьИнтернета
    {
        get { return БазоваяСкоростьИнтернета / КоличествоУстройств; }
    }
}

class Орк
{
    public static int КоличествоОрков { get; private set; }
    public int КоличествоПереносимогоЗолота { get; private set; }

    public Орк()
    {
        КоличествоОрков++;
        КоличествоПереносимогоЗолота += (КоличествоОрков <= 5) ? 2 : -2;
        КоличествоПереносимогоЗолота = Math.Max(0, КоличествоПереносимогоЗолота);
    }
}

class Программа
{
    static void Main()
    {
        // Пример использования класса "Волшебник" и "Заклинание"
        Заклинание алохомора = new Заклинание(60, "Замок открывается!");
        Заклинание вингардиумЛевиоса = new Заклинание(60, "Предмет поднимается в воздух!");

        Волшебник гарриПоттер = new Волшебник("Гарри Поттер", 100);
        гарриПоттер.ПроизнестиЗаклинание(алохомора);
        гарриПоттер.ПроизнестиЗаклинание(вингардиумЛевиоса);

        // Пример использования класса "Служба отправки" и "Посылка"
        Посылка посылка1 = new Посылка("Книга", 2.5);
        Посылка посылка2 = new Посылка("Электроника", 4.0);

        СлужбаОтправки почта = new СлужбаОтправки(5.0);
        почта.ОтправитьПосылку(посылка1);
        почта.ОтправитьПосылку(посылка2);

        // Пример использования класса "Устройство"
        Устройство устройство1 = new Устройство();
        Устройство устройство2 = new Устройство();
        Console.WriteLine($"Скорость интернета устройства 1: {устройство1.СкоростьИнтернета}");
        Console.WriteLine($"Скорость интернета устройства 2: {устройство2.СкоростьИнтернета}");

        // Пример использования класса "Орк"
        for (int i = 0; i < 7; i++)
        {
            Орк орк = new Орк();
            Console.WriteLine($"Орк {i + 1}: Переносимое золото - {орк.КоличествоПереносимогоЗолота}");
        }

        Console.ReadKey(true);
    }
}